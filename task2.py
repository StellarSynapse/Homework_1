# 'hello world'
print("hello world")

# 'hello', 'world' with a space separator
print("hello", "world", sep=" ")

# 'hello', 'world' with a dash separator
print("hello", "world", sep="-")

# 'hello world' with a newline at the end
print("hello world", end="\n")

# 'hello world' with '!!!' at the end
print("hello world", end="!!!\n")

# 'hello' and 'world' with ' ' separator and '!!!' at the end
print("hello", "world", sep=" ", end="!!!\n")
